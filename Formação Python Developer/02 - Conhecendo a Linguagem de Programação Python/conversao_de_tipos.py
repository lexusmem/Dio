# inteiro para ponto flutuante - float
preco = 10
print(preco)
preco = float(preco)
print(preco)
preco = 10/2
print(preco)

# float para inteiro
preco = 10.3
print(preco)
preco = int(preco)
print(preco)

# conversão por divisão
preco = 10
print(preco)
print(preco / 2)
print(preco // 2)

#  número para strin
preco = 10.50
idade = 28
print(str(preco))
print(str(idade))
texto = f'idade {idade} preco {preco}'
print(texto)

# string para número
