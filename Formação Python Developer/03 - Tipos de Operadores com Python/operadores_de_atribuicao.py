# definir valores inicial ou subscrever valores de uma variável

# atribuição simples
saldo = 500
print(saldo)

# atribuição com adição
saldo += 200
print(saldo)

# atribuição com exponenciação
saldo **= 4
print(saldo)

# atribuição com divisão inteira
saldo //= 3
print(saldo)

# atribuição com multiplicação
saldo *= 1.5
print(saldo)

# atribuição com subtração
saldo -= 100
print(saldo)

# atribuição com divisão
saldo /= 2
print(saldo)

# atribuição com modulo
saldo %= 3
print(saldo)
