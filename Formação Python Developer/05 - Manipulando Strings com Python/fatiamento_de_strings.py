# retornar partes de uma string
nome = 'alex silva de sousa'
print(nome[0])  # primeira letra
print(nome[-1])  # última letra

print(nome[:4])  # retorna até o carácter de 0 a 3

print(nome[4:])  # retorna a partir do carácter 4

print(nome[5:11])  # retorna meio de string

print(nome[5:11:2])  # retorna meio de string com passos

print(nome[:])  # copia uma string

print(nome[:: -1])  # cria copia invertida / espelhada
