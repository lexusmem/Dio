# ORM - object relational mapping

'''
ORM, ou Mapeamento Objeto-Relacional (Object-Relational Mapping), é uma técnica de programação usada para converter
dados entre sistemas incompatíveis usando linguagens de programação orientadas a objetos. Em termos simples, o ORM
permite que você interaja com um banco de dados relacional usando objetos da sua linguagem de programação, sem precisar escrever SQL diretamente.

Como Funciona o ORM
O ORM funciona mapeando classes da linguagem de programação para tabelas no banco de dados.
Cada instância de uma classe corresponde a uma linha na tabela do banco de dados,
e cada atributo da classe corresponde a uma coluna na tabela.
'''

