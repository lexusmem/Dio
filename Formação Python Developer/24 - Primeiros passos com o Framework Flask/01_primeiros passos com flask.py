# Flask

'''
O Flask é um microframework para desenvolvimento web escrito em Python,
conhecido por sua simplicidade e flexibilidade. Ele permite que você crie
sites, aplicativos web e APIs de forma rápida e eficiente, com a possibilidade
de adicionar funcionalidades gradualmente.

não requer ferramentas ou bibliotecas particulares.
'''

'''
Flask-SQLAlchemy é uma extensão para o framework Flask que simplifica a
integração do popular ORM (Object Relational Mapper) SQLAlchemy com aplicações
Flask.
O SQLAlchemy é uma biblioteca Python poderosa que permite interagir com bancos
de dados relacionais usando Python. Ele abstrai as diferenças entre diferentes
bancos de dados (como MySQL, PostgreSQL, SQLite) e permite que você trabalhe
com seus dados usando objetos Python.
'''
