'''
Pilares da segurança da informação:

Confidencialidade: As informações devem ser acessíveis apenas por pessoas
autorizadas. É como um segredo que só algumas pessoas podem conhecer.

Integridade: As informações devem ser precisas e completas, sem alterações não
autorizadas. Imagine um documento importante que não pode ser alterado por
ninguém sem permissão.

Disponibilidade: As informações devem estar acessíveis quando e onde forem
necessárias. É como ter um arquivo importante sempre que você precisar.

Autenticidade: A identidade de quem envia ou recebe as informações deve ser
verificada. É como ter um passaporte que prova quem você é.

Não repúdio: Impossibilidade de negar a autoria de uma informação. É como
deixar um rastro digital que prova que você fez algo.

Identificação: É o processo de estabelecer e verificar a identidade de um
usuário ou dispositivo. É como apresentar um documento de identificação para provar quem você é.

Autorização: É o processo de determinar quais recursos ou ações um usuário
autenticado pode acessar. É como receber um crachá que te dá acesso a
determinadas áreas de um prédio.
'''
